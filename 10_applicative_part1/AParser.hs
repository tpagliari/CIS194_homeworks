-- Functor instance for Parser
instance Functor Parser where
  fmap :: (a -> b) -> Parser a -> Parser b
  fmap h (Parser f) = Parser (first h . f)

first :: (a -> b) -> Maybe (a,c) -> Maybe (b,c)
first h = fmap h'
  where h' (x, y) = (h x, y)

